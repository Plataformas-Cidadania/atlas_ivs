    <h2> Regiões Metropolitanas </h2>
	
    <p>O cruzamento das faixas do IDHM e do IVS, que gera a Prosperidade Social, é apresentado no gráfico, a seguir. Nele está apontado o número de regiões metropolitanas (RMs) que se encontram em cada faixa (cor) da Prosperidade Social em 2010. </p>
    <img src="./assets/img/prosperidade/rm/matriz.png" style="height:300px; float:left;">
    <p>Nas faixas mais elevadas da Prosperidade Social (alta e muito alta), em 2010, há um total de 18 RMs que conjugam médio ou um alto/muito alto Desenvolvimento Humano, com uma média ou baixa/muito baixa Vulnerabilidade Social.</p>
    <p>No outro extremo do mesmo gráfico, nas faixas mais baixas da Prosperidade Social (baixa e muito baixa), em 2010, não há nenhuma RM que apresente baixo/muito baixo ou médio Desenvolvimento Humano, combinados com alta/muito alta ou média Vulnerabilidade Social. A faixa de média Prosperidade Social, em 2010, congrega duas RMs.</p>
    
    <p>Destas, uma apresentou a combinação de alto Desenvolvimento Humano com alta Vulnerabilidade Social. Nesta localidade verifica-se que a condição de elevado Desenvolvimento Humano (educação, longevidade e renda) não se reverteram na redução do quadro de Vulnerabilidade Social instalada. </p>
    <p style="clear:both;">A tabela e o gráfico abaixo trazem a comparação para o período 2000/2010.</p>
