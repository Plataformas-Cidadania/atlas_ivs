    <h2>A PROSPERIDADE SOCIAL NAS UNIDADES DE DESENVOLVIMENTO HUMANO BRASILEIRAS</h2>
	
    <p>O cruzamento das faixas do IDHM e do IVS, que gera a Prosperidade Social, é apresentado no gráfico, a seguir. Nele está apontado o número de Unidades de Desenvolvimento Humano (UDHs) que se encontram em cada faixa (cor) da Prosperidade Social em 2010.  </p>
    <img src="./assets/img/prosperidade/udh/matriz.png" style="height:300px; float:left;">
    <p>Nas faixas mais elevadas da Prosperidade Social (alta e muito alta), em 2010, há um total de 7.531 UDHs que conjugam médio ou um alto/muito alto Desenvolvimento Humano, com uma  <em>média</em>  ou <em>baixa/muito baixa</em> Vulnerabilidade Social. </p>
    <p>No outro extremo do mesmo gráfico, nas faixas mais baixas da Prosperidade Social (baixa e muito baixa), em 2010, há 1.889 UDHs com baixo/muito baixo ou médio Desenvolvimento Humano, combinados com <em>alta/muito alta</em> ou  <em>média</em>  Vulnerabilidade Social. A faixa de  <em>média</em>  Prosperidade Social, em 2010, congrega 1.702 UDHs.</p>
    
    <p>Destas, 41 apresentaram a combinação de alto Desenvolvimento Humano com <em>alta</em> Vulnerabilidade Social. Nestas localidades verifica-se que a condição de elevado Desenvolvimento Humano (educação, longevidade e renda) não se reverteram na redução do quadro de Vulnerabilidade Social instalada.</p>
    <p style="clear:both;">A tabela e o gráfico abaixo trazem a comparação para o período 2000/2010.</p>
    <img src="./assets/img/prosperidade/udh/tabela.png" style="width:700px;">
    <img src="./assets/img/prosperidade/udh/grafico.png" style="width:700px;">
    <p>Observa-se um aumento do número de UDHs na faixa da <em>muito alta</em> Prosperidade Social, em 2000, 1.967 UDHs (18%) se encontravam nesta faixa, que passa a abrigar 5.131 UDHs (46%), em 2010. </p>
    <p>No outro extremo, observa-se uma redução no número de UDHs que se encontram na faixa da <em>muito baixa</em> Prosperidade Social, eram 3.866 UDHs (36%), em 2000, número reduzido para 233 (2%), em 2010.</p>


    
