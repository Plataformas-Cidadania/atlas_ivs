    <h2> A PROSPERIDADE SOCIAL NOS MUNICÍPIOS BRASILEIROS </h2>
    <p>O cruzamento das faixas do IDHM e do IVS, que gera a Prosperidade Social, é apresentado no gráfico, a seguir. Nele está apontado o número de municípios brasileiros que se encontram em cada faixa (cor) da Prosperidade Social em 2010. </p>
    <img src="./assets/img/prosperidade/municipio/matriz.png" style="height:300px; float:left;">
    <p>Nas faixas mais elevadas da Prosperidade Social (<em>alta</em> e <em>muito alta</em>), em 2010, há um total de 2.569 municípios que conjugam <em>médio</em> ou um <em>alto/muito alto</em> Desenvolvimento Humano, com uma <em>média</em> ou <em>baixa/muito baixa</em> Vulnerabilidade Social. </p>
    <p>No outro extremo do mesmo gráfico, nas faixas mais baixas da Prosperidade Social (<em>baixa</em> e <em>muito baixa</em>), em 2010, concentram-se 2.073 municípios que apresentam <em>baixo/muito baixo</em> ou <em>médio</em> Desenvolvimento Humano, combinados com <em>alta/muito alta</em> ou <em>média</em> Vulnerabilidade Social. A faixa de <em>média</em> Prosperidade Social, em 2010, congrega outros 923 municípios. </p>
    
    <p>Destes, cinco apresentam a combinação de <em>alto</em> Desenvolvimento Humano com <em>alta</em> Vulnerabilidade Social. Nestes municípios verifica-se que a condição de elevado Desenvolvimento Humano (educação, longevidade e renda) não se reverteram na redução do quadro de Vulnerabilidade Social instalada. </p>
    <p style="clear:both;">A tabela e o gráfico abaixo trazem a comparação para o período 2000/2010.</p>
    <img src="./assets/img/prosperidade/municipio/tabela.png" style="width:600px;">
    <img src="./assets/img/prosperidade/municipio/grafico.png" style="width:600px;">
    <p>Observa-se um aumento do número de municípios na faixa da <em>muito alta</em> Prosperidade Social, em 2000, apenas 103 municípios (cerca de 2%) se encontravam nesta faixa, que passa a abrigar 1.685 municípios (30%), em 2010. </p>
    <p>No outro extremo, observa-se uma redução no número de municípios que se encontram na faixa da <em>muito baixa</em> Prosperidade Social, eram 3.415 municípios brasileiros (62%), em 2000, número reduzido para 1.302 (23%), em 2010.</p>


    
