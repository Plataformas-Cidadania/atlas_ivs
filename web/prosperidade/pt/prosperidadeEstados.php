    <h2> A PROSPERIDADE SOCIAL NOS ESTADOS BRASILEIROS </h2>
	
    <p>O cruzamento das faixas do IDHM e do IVS, que gera a Prosperidade Social, é apresentado no gráfico, a seguir. Nele está apontado o número de estados brasileiros que se encontram em cada faixa (cor) da Prosperidade Social em 2010.  </p>
    <img src="./assets/img/prosperidade/estado/matriz.png" style="height:300px; float:left;">
    <p>Nas faixas mais elevadas da Prosperidade Social (<em>alta</em> e <em>muito alta</em>), em 2010, há um total de 12 estados que conjugam <em>médio</em> ou um <em>alto/muito alto</em> Desenvolvimento Humano, com uma <em>média</em> ou <em>baixa/muito baixa</em> Vulnerabilidade Social.  </p>
    <p>No outro extremo do mesmo gráfico, nas faixas mais baixas da Prosperidade Social (<em>baixa e muito baixa</em>), em 2010, concentram-se 8 estados que apresentam <em>baixo/muito baixo</em> ou <em>médio</em> Desenvolvimento Humano, combinados com <em>alta/muito alta</em> ou <em>média</em> Vulnerabilidade Social. A faixa de <em>média</em> Prosperidade Social, em 2010, congrega outros 7 estados. </p>
    
    <p>Destes, um apresentou a combinação de <em>alto</em> Desenvolvimento Humano com <em>alta</em> Vulnerabilidade Social. Nestas localidades verifica-se que a condição de elevado Desenvolvimento Humano (educação, longevidade e renda) não se reverteram na redução do quadro de Vulnerabilidade Social instalada.  </p>
    <p style="clear:both;">A tabela e o gráfico abaixo trazem a comparação para o período 2000/2010.</p>
    <img src="./assets/img/prosperidade/estado/tabela.png" style="width:600px;">
    <img src="./assets/img/prosperidade/estado/grafico.png" style="width:600px;">
    <p>Observa-se um aumento do número de estados na faixa da <em>muito alta</em> Prosperidade Social, em 2000 nenhum estado se encontravam nesta faixa, que passa a abrigar 9 estados (33%), em 2010.  </p>
    <p>No outro extremo, observa-se uma redução no número de estados que se encontram na faixa da <em>muito baixa</em> Prosperidade Social, eram 16 estados brasileiros (59%), em 2000, número reduzido para zero, em 2010.</p>


    
